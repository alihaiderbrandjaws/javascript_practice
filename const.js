// In JavaScript, const is used to declare variables that cannot be reassigned after their initial assignment. It is commonly used for values that should remain constant throughout the execution of the program.
// Block Scope – Similar to let, const is only accessible within the block {} where it is declared.
// No Reassignment – Once assigned a value, a const variable cannot be changed.
// Must Be Initialized – A const variable must be assigned a value at the time of declaration //

const myVariable = "Hello, World!";
console.log(myVariable);